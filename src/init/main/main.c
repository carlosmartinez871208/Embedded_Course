int main (void)
{
    Port_Init(&((Port_ConfigType){.PinConfigList = (Pin_ConfigType[]){GREEN_LED,PUSH_BUTTON},.NumberOfPins = 2}));
    while(TRUE)
    {
        if (Port_GetPinState(&PUSH_BUTTON))
        {
            Port_SetPinState(&GREEN_LED,STD_HIGH);
        }
        else
        {
            Port_SetPinState(&GREEN_LED,STD_LOW);
        }
    }
    return EXIT_SUCCESS;
}

