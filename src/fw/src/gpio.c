void GPIO_config_pin(void)
{
    // Example: Configure PA5 as output
    GPIOA_TYPE->MODER &= ~GPIO_PORTA_MODER_5_1; // Clear mode bits for PA5
    GPIOA_TYPE->MODER |=  GPIO_PORTA_MODER_5_0;  // Set PA5 as output
}

void GPIO_toggle(void)
{
    // Example: Toggle PA5
    GPIOA_TYPE->ODR ^= GPIO_PORTA_ODR_5; // Toggle PA5
}

